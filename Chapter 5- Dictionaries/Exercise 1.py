# Use a dictionary to store information about a person you know.

# Store their first name, last name, age, and the city in which they live. 

# You should have keys such as first_name, last_name, age, and city. Print each piece of information stored in your dictionary.


# Information about the person, storing it in a dictionary

brother = {
    'first_name': 'Ahmad',
    'last_name': 'Asif',
    'age': '18',
    'city': 'Dubai',
}

# Printing information stored in dictionary

print(brother['first_name'])

print(brother['last_name'])

print(brother['age'])

print(brother['city'])
